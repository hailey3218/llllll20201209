#include "libht.h"

char		*ft_strdup(cjar *s)
{
	char *p;
	size_t i;

	p = (char *)malloc(ft_strlen(s) + 1);
	i = 0;
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
